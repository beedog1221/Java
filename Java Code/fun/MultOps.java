// exercise 4, print totals for multiple equations 

class MultOps{public static void main(String args[]){
	double total;
	
	total = -5 + 8 * 6;
	System.out.println("-5 + 8 * 6 = " + total);
	
	total = (55 +9) % 9;
	System.out.println("(55 +9) % 9 = " + total);
	
	total = 20 + -3 * 5 / 8;
	System.out.println("20 + -3 * 5 / 8 = " + total);
	
	total = 5 + 15 / 3 * 2 - 8 % 3;
	System.out.println("5 + 15 / 3 * 2 - 8 % 3 = " + total);
}
}